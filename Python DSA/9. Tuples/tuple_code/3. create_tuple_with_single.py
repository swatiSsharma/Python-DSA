'''
A program to create and find type a tuple with a single input value.
'''

# Creating a Tuple
tup1 = eval(input("Enter the value for tuple: "))  # 67, 
print("Tuple created is :", tup1)

# finding the type of tuple
tup2 = 67, 
print("Tuple created is :", type(tup2), tup2)

tup3 = 67 
print("Tuple created is :", type(tup3), tup3)