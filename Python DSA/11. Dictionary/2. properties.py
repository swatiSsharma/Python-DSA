# Properties of Dictionary

'''
Dictionaries in Python have several key properties that make them useful for various programming 
tasks. Here are some of the key properties of dictionaries:
'''
## Unordered:
'''
The elements in a dictionary are not ordered, meaning there is no guaranteed order of the items. 
This is different from lists, where elements have a specific order.
'''
## Mutable:
'''
Dictionaries are mutable, which means you can add, modify, or remove key-value pairs after the 
dictionary is created.
'''
## Key-Value Pairs:
'''
Dictionaries consist of key-value pairs, where each key must be unique. The keys are used to access 
the associated values.
'''
## Dynamic Size:
'''
Dictionaries can dynamically grow or shrink in size as key-value pairs are added or removed. This 
makes them flexible for handling varying amounts of data.
'''
## Heterogeneous Data:
'''
Both keys and values in a dictionary can be of any data type, and they can be mixed within the same 
dictionary. This allows for flexibility in representing complex data structures.
'''
## Efficient Access:
'''
Retrieving a value from a dictionary is generally an O(1) operation, making dictionary lookups very 
efficient. This is because dictionaries use hash tables for fast key-based access.
'''
## No Duplicate Keys:
'''
Each key in a dictionary must be unique. If you try to add a key that already exists, it will 
overwrite the existing value associated with that key.
'''
## Key Immutability:
'''
Keys must be immutable, meaning they cannot be changed after they are created. This is why common 
key types include strings, numbers, and tuples (if they only contain immutable elements).
'''
## Versatility:
'''
Dictionaries are versatile and can be used for a wide range of tasks, such as storing configuration 
settings, representing JSON-like structures, and efficiently looking up values based on keys.
'''