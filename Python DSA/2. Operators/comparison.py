'''
Comparison Operator

Comparison operators in Python are used to compare values. They return a Boolean result (True or False) based on the comparison.

'''

a = 10
b = 10

print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)
print(a != b)

a = 20
b = 15
c = 20
print(a < b < c)
print(a > b > c)
print(a <= b <= c)
print(a >= b >= c)
print(a == b == c)
print(a != b != c)

